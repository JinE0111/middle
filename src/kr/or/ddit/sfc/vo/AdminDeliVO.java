package kr.or.ddit.sfc.vo;

public class AdminDeliVO {
	private String memId;
	private String buyNo;
	private String buyDate;
	private String deliStatCode;
	private String deliStat;
	private int rnum;
	
	
	public int getRnum() {
		return rnum;
	}
	public void setRnum(int rnum) {
		this.rnum = rnum;
	}
	public String getMemId() {
		return memId;
	}
	public void setMemId(String memId) {
		this.memId = memId;
	}
	public String getBuyNo() {
		return buyNo;
	}
	public void setBuyNo(String buyNo) {
		this.buyNo = buyNo;
	}
	public String getBuyDate() {
		return buyDate;
	}
	public void setBuyDate(String buyDate) {
		this.buyDate = buyDate;
	}
	public String getDeliStatCode() {
		return deliStatCode;
	}
	public void setDeliStatCode(String deliStatCode) {
		this.deliStatCode = deliStatCode;
	}
	public String getDeliStat() {
		return deliStat;
	}
	public void setDeliStat(String deliStat) {
		this.deliStat = deliStat;
	}
	
	
	
}
